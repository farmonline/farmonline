package br.com.farmonline.mapa;

public class MapLocationPharma {
	
	private String nomeEstabelecimento;
	private String endereco;
	private String linkMapaGoogle;	
	private String urlImage;

	public String getNomeEstabelecimento() {
		return nomeEstabelecimento;
	}

	public void setNomeEstabelecimento(String nomeEstabelecimento) {
		this.nomeEstabelecimento = nomeEstabelecimento;
	}

	public String getEndereco() {
		return endereco;
	}

	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}

	public String getLinkMapaGoogle() {
		return linkMapaGoogle;
	}

	public void setLinkMapaGoogle(String linkMapaGoogle) {
		this.linkMapaGoogle = linkMapaGoogle;
	}

	public String getUrlImage() {
		return urlImage;
	}

	public void setUrlImage(String urlImage) {
		this.urlImage = urlImage;
	}    

}
